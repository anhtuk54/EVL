#!/bin/bash
################################################################################
# Basic settings
################################################################################
source ENV.sh

################################################################################
# Configurations
################################################################################
mkdir -p ${DROPBEAR_BUILD_DIR}
cd ${DROPBEAR_BUILD_DIR}
${DROPBEAR_DIR}/configure --host=aarch64-linux-gnu \
	--prefix=${DROPBEAR_INSTALL_DIR} \
	--disable-zlib --enable-static \
	CC=aarch64-linux-gnu-gcc LD=aarch64-linux-gnu-ld

################################################################################
# For building
################################################################################
make -j6 install
